
public class Exemplo03 {

	public static void main(String[] args) {
		int contador = 0;
		
		System.out.println("Exemplo - Do While");
		do {
			System.out.println(contador);
			contador++;
			
		}while(contador > 1);
		
		
		System.out.println("Exemplo - While");
		contador = 0;
		
		while(contador > 1) {
			System.out.println(contador);
			contador++;
		}

	}

}
